            public MT5CryptAes256(byte[] key, int len = 32)
            {
                Init(key, len);
            }

            public void Init(byte[] aesKey, int len = 32)
            {
                if (aesKey == null) throw new ArgumentNullException(nameof(aesKey));
                if (len != 16 && len != 24 && len != 32) throw new ArgumentException("len must be 16, 24, or 32 bytes.", nameof(len));
                if (aesKey.Length < len) throw new ArgumentException($"Key must be at least {len} bytes.", nameof(aesKey));

                _nk = len / 4;
                _nr = _nk + Nb + 2;

                if (_nk != 4 && _nk != 6 && _nk != 8)
                    throw new InvalidOperationException("Key size must be 128, 192, or 256 bits.");

                _w = new uint[Nb * (_nr + 1)];
                _state = new byte[4, Nb];

                KeyExpansion(aesKey, len);
            }

            /// <summary>
            /// Encrypts exactly one 16-byte block.
            /// </summary>
            public byte[] EncryptBlock(byte[] block16)
            {
                if (block16 == null) throw new ArgumentNullException(nameof(block16));
                if (block16.Length != 16) throw new ArgumentException("Block must be exactly 16 bytes.", nameof(block16));

                // Place input into state matrix in column order
                for (int i = 0; i < 4 * Nb; i++)
                {
                    _state[i % 4, i / Nb] = block16[i];
                }

                AddRoundKey(0);

                for (int round = 1; round < _nr; round++)
                {
                    SubBytes();
                    ShiftRows();
                    MixColumns();
                    AddRoundKey(round);
                }

                SubBytes();
                ShiftRows();
                AddRoundKey(_nr);

                // Extract state matrix into output in column order
                var outBlock = new byte[16];
                for (int i = 0; i < 4 * Nb; i++)
                {
                    outBlock[i] = _state[i % 4, i / Nb];
                }

                return outBlock;
            }

            public void Dispose()
            {
                if (_w != null) Array.Clear(_w, 0, _w.Length);
                if (_state != null) Array.Clear(_state, 0, _state.Length);
            }

            private void KeyExpansion(byte[] key, int len)
            {
                // Rcon is the round constant
                uint[] rcon =
                {
                0x00000000u, 0x01000000u, 0x02000000u, 0x04000000u, 0x08000000u, 0x10000000u, 0x20000000u,
                0x40000000u, 0x80000000u, 0x1b000000u, 0x36000000u, 0x6c000000u, 0xd8000000u, 0xab000000u,
                0x4d000000u, 0x9a000000u, 0x2f000000u
            };

                // first Nk words are the cipher key
                for (int i = 0; i < _nk; i++)
                {
                    _w[i] = 0;
                    _w[i] |= (uint)key[4 * i] << 24;
                    _w[i] |= (uint)key[4 * i + 1] << 16;
                    _w[i] |= (uint)key[4 * i + 2] << 8;
                    _w[i] |= (uint)key[4 * i + 3];
                }

                int totalWords = Nb * (_nr + 1);
                for (int i = _nk; i < totalWords; i++)
                {
                    uint temp = _w[i - 1];

                    if (i % _nk == 0)
                        temp = SubWord(RotWord(temp)) ^ rcon[i / _nk];
                    else if (_nk > 6 && (i % _nk) == 4)
                        temp = SubWord(temp);

                    _w[i] = _w[i - _nk] ^ temp;
                    _w[i] &= 0xFFFFFFFFu;
                }
            }

            private void AddRoundKey(int round)
            {
                for (int i = 0; i < 4; i++)
                {
                    for (int j = 0; j < Nb; j++)
                    {
                        // i-th byte of j-th word in this round
                        int shift = (3 - i) * 8;
                        byte temp = (byte)((_w[round * Nb + j] >> shift) & 0xFF);
                        _state[i, j] ^= temp;
                    }
                }
            }

            private void SubBytes()
            {
                for (int i = 0; i < 4; i++)
                    for (int j = 0; j < Nb; j++)
                        _state[i, j] = SBox[_state[i, j]];
            }

            private void ShiftRows()
            {
                // row 0 unchanged
                for (int i = 1; i < 4; i++)
                {
                    byte[] temp = new byte[Nb];
                    for (int j = 0; j < Nb; j++)
                        temp[j] = _state[i, (j + i) % Nb];

                    for (int j = 0; j < Nb; j++)
                        _state[i, j] = temp[j];
                }
            }

            private void MixColumns()
            {
                for (int c = 0; c < Nb; c++)
                {
                    byte s0 = _state[0, c];
                    byte s1 = _state[1, c];
                    byte s2 = _state[2, c];
                    byte s3 = _state[3, c];

                    _state[0, c] = (byte)(Mult(0x02, s0) ^ Mult(0x03, s1) ^ Mult(0x01, s2) ^ Mult(0x01, s3));
                    _state[1, c] = (byte)(Mult(0x01, s0) ^ Mult(0x02, s1) ^ Mult(0x03, s2) ^ Mult(0x01, s3));
                    _state[2, c] = (byte)(Mult(0x01, s0) ^ Mult(0x01, s1) ^ Mult(0x02, s2) ^ Mult(0x03, s3));
                    _state[3, c] = (byte)(Mult(0x03, s0) ^ Mult(0x01, s1) ^ Mult(0x01, s2) ^ Mult(0x02, s3));
                }
            }
            private void InvSubBytes()
            {
                for (int i = 0; i < 4; i++)
                    for (int j = 0; j < Nb; j++)
                        _state[i, j] = InvSBox[_state[i, j]];
            }

            private void InvShiftRows()
            {
                for (int i = 1; i < 4; i++)
                {
                    byte[] temp = new byte[Nb];
                    for (int j = 0; j < Nb; j++)
                        temp[(i + j) % Nb] = _state[i, j];

                    for (int j = 0; j < Nb; j++)
                        _state[i, j] = temp[j];
                }
            }

            private void InvMixColumns()
            {
                for (int c = 0; c < Nb; c++)
                {
                    byte s0 = _state[0, c];
                    byte s1 = _state[1, c];
                    byte s2 = _state[2, c];
                    byte s3 = _state[3, c];

                    _state[0, c] = (byte)(Mult(0x0e, s0) ^ Mult(0x0b, s1) ^ Mult(0x0d, s2) ^ Mult(0x09, s3));
                    _state[1, c] = (byte)(Mult(0x09, s0) ^ Mult(0x0e, s1) ^ Mult(0x0b, s2) ^ Mult(0x0d, s3));
                    _state[2, c] = (byte)(Mult(0x0d, s0) ^ Mult(0x09, s1) ^ Mult(0x0e, s2) ^ Mult(0x0b, s3));
                    _state[3, c] = (byte)(Mult(0x0b, s0) ^ Mult(0x0d, s1) ^ Mult(0x09, s2) ^ Mult(0x0e, s3));
                }
            }

            // GF(2^8) multiply using the provided log/antilog tables (same as PHP)
            private static byte Mult(byte a, byte b)
            {
                if (a == 0 || b == 0) return 0;

                int sum = LTable[a] + LTable[b];
                sum %= 255;
                return ATable[sum];
            }

            private static uint RotWord(uint w)
            {
                uint temp = (w >> 24) & 0xFF;
                w = (w << 8) & 0xFFFFFFFFu;
                w += temp;
                return w & 0xFFFFFFFFu;
            }

            private static uint SubWord(uint w)
            {
                uint outw = 0;
                for (int i = 0; i < 4; i++)
                {
                    byte b = (byte)((w >> 24) & 0xFF);
                    w = (w << 8) & 0xFFFFFFFFu;
                    outw = (outw << 8) | SBox[b];
                }
                return outw & 0xFFFFFFFFu;
            }
        }
